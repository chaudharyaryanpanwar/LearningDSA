int findMissingNumber(vector<int> &arr, int n)
{
    int sum = 0;
    for (int i = 0; i < n; i++)
    {
        sum += arr[i];
    }
    return (n * (n + 1) / 2) - sum;
}
int main() {
    int n = 5;
    vector<int> arr = {1, 2, 3, 4, 6};
    cout << findMissingNumber(arr, n) << endl;
    return 0;
}